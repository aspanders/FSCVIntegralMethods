def letter_pattern(ch, color="black"):
    # place the 5x7 glyph with a 1-cell margin -> 7x9 grid
    rows = ["Бқ§" + r + "Бқ§" for r in FONT[ch]]
    rows = ["Бқ§Бқ§Бқ§Бқ§Бқ§Бқ§Бқ§"] + rows + ["Бқ§Бқ§Бқ§Бқ§Бқ§Бқ§Бқ§"]
    cells = cells_from_ascii(rows, {"#": color})
    kind = "letter" if ch.isalpha() else "number"
    return make_pattern(
        stable_id("icon", f"{kind}-{ch}"),
        f"Letter {ch}" if ch.isalpha() else f"Number {ch}",
        "icons", 7, 9, cells,
        tags=[kind, ch.lower(), "text", "alphabet" if ch.isalpha() else "digit"],
        difficulty="easy",
    )

