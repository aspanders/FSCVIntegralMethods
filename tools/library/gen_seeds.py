"""Emit SeedPatterns.kt and SeedPatterns.swift from the generated library.

The compiled seed set is only an offline fallback: the full library ships as a
bundled asset (library.json) and refreshes over the network. So we keep it tiny,
one compact, representative pattern per category, and generate it so it always
matches the current taxonomy and renders identically on both platforms.
"""
import os

import gen_icons
import gen_library

HERE = os.path.dirname(os.path.abspath(__file__))
REPO = os.path.abspath(os.path.join(HERE, "..", ".."))
KT = os.path.join(REPO, "BeadSnapAndroid", "app", "src", "main", "kotlin",
                  "com", "beadsnap", "app", "data", "model", "SeedPatterns.kt")
SW = os.path.join(REPO, "BeadSnap", "BeadSnap", "Seeds", "SeedPatterns.swift")

# One hand-picked, recognizable, compact example per category.
PICKS = {
    "hearts":    lambda ps: _named(ps, "Red Heart"),
    "stars":     lambda ps: _named(ps, "5-Point Star 1"),
    "flowers":   lambda ps: _smallest(ps, "sunflower"),
    "emoji":     lambda ps: _named(ps, "Happy Face 1"),
    "gems":      lambda ps: _named(ps, "Heart Gem 1"),
    "rainbows":  lambda ps: _named(ps, "Rainbow Arc 1"),
    "space":     lambda ps: _named(ps, "Rocket 2"),
    "mandalas":  lambda ps: _smallest(ps),
    "geometric": lambda ps: _smallest(ps),
    "icons":     None,   # taken from gen_icons below
}
NAMES = {"hearts": "heart", "stars": "star", "flowers": "flower", "emoji": "smiley",
         "gems": "gem", "rainbows": "rainbow", "space": "rocket", "mandalas": "mandala",
         "geometric": "tile", "icons": "letter"}


def _named(ps, title):
    for p in ps:
        if p["title"] == title:
            return p
    return _smallest(ps)


def _smallest(ps, tag=None):
    cand = [p for p in ps if (tag is None or tag in p.get("tags", []))]
    cand = [p for p in (cand or ps) if max(p["grid"]["width"], p["grid"]["height"]) <= 29]
    return min(cand, key=lambda p: len(p["cells"]))


def collect_seeds():
    seeds = {}
    for cat, fn in gen_library.GENERATORS.items():
        ps = fn()
        seeds[cat] = PICKS[cat](ps)
    # icons: a clean letter A
    seeds["icons"] = _named(gen_icons.generate(), "Letter A")
    return [seeds[c] for c in
            ["geometric", "mandalas", "hearts", "stars", "flowers",
             "rainbows", "space", "emoji", "gems", "icons"]]


DIFF = {"easy": "easy", "medium": "medium", "hard": "hard"}


def emit_kotlin(seeds):
    L = ["package com.beadsnap.app.data.model", "",
         "// AUTO-GENERATED by tools/library/gen_seeds.py. Do not edit by hand.",
         "// A tiny offline taster: one pattern per category. The full library",
         "// ships as a bundled asset (library.json) and refreshes over the network.",
         "object SeedPatterns {", ""]
    names = []
    for p in seeds:
        nm = NAMES[p["category"]]
        names.append(nm)
        pal = ", ".join(f'BeadColor("{c["id"]}","{c["name"]}","{c["hex"]}")' for c in p["palette"])
        cells = ", ".join(f'Cell({c["x"]},{c["y"]},"{c["colorId"]}")' for c in p["cells"])
        tags = ", ".join(f'"{t}"' for t in p["tags"])
        L += [f'    private val {nm} = FusePattern(',
              f'        id = "{p["id"]}", title = "{p["title"]}",',
              f'        category = PatternCategory.{p["category"]}, createdBy = CreatorType.system,',
              f'        grid = GridSize({p["grid"]["width"]}, {p["grid"]["height"]}), '
              f'difficulty = Difficulty.{DIFF[p["difficulty"]]},',
              f'        palette = listOf({pal}),',
              f'        tags = listOf({tags}),',
              f'        cells = listOf({cells}),',
              f'        version = 1',
              f'    )', ""]
    L += [f'    val all: List<FusePattern> = listOf({", ".join(names)})', "}", ""]
    return "\n".join(L)


def emit_swift(seeds):
    L = ["import Foundation", "",
         "// AUTO-GENERATED by tools/library/gen_seeds.py. Do not edit by hand.",
         "// A tiny offline taster: one pattern per category. The full library",
         "// ships as a bundled asset (library.json) and refreshes over the network.",
         "enum SeedPatterns {", ""]
    names = []
    for p in seeds:
        nm = NAMES[p["category"]]
        names.append(nm)
        pal = ", ".join(f'PaletteColor(id: "{c["id"]}", name: "{c["name"]}", hex: "{c["hex"]}")'
                        for c in p["palette"])
        cells = ", ".join(f'Cell(x: {c["x"]}, y: {c["y"]}, colorId: "{c["colorId"]}")'
                          for c in p["cells"])
        tags = ", ".join(f'"{t}"' for t in p["tags"])
        L += [f'    static let {nm} = FusePattern(',
              f'        id: "{p["id"]}", title: "{p["title"]}",',
              f'        category: .{p["category"]}, createdBy: .system,',
              f'        grid: GridSize(width: {p["grid"]["width"]}, height: {p["grid"]["height"]}),',
              f'        palette: [{pal}],',
              f'        cells: [{cells}],',
              f'        difficulty: .{DIFF[p["difficulty"]]}, tags: [{tags}],',
              f'        version: 1',
              f'    )', ""]
    L += [f'    static let all: [FusePattern] = [{", ".join(names)}]', "}", ""]
    return "\n".join(L)


if __name__ == "__main__":
    seeds = collect_seeds()
    open(KT, "w").write(emit_kotlin(seeds))
    open(SW, "w").write(emit_swift(seeds))
    tot = sum(len(p["cells"]) for p in seeds)
    print(f"wrote {len(seeds)} seeds ({tot} cells total)")
    for p in seeds:
        print(f'  {p["category"]:10} {p["title"]:20} {len(p["cells"])} cells')
